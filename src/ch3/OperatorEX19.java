package ch3;

public class OperatorEX19 {
    public static void main(String[] args) {
        int x = 10;
		int y = 8;

		System.out.printf("%d을 %d로 나누면, \n", x, y); 
		System.out.printf("몫은 %d이고, 나머지는 %d입니다.\n", x / y, x % y); 
    }
}

